La Sirena torna a cantare in quella magnifica cornice che è il comune abruzzese: uno scenario perfetto, come molti negli anni passati hanno descritto, in cui non solo vivere le emozioni di un concerto ma soprattutto quella di un'esperienza diversa, unica. Un festival che non vuole essere solo festival ma un momento da attraversare, se possibile, con tutti i sensi.</p>
