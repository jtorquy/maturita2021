                <h5>Festival della ventricina<br/><em>12-14 luglio</em></h5>
                <p>Il Festival si articolerà in una parte espositiva e in una ristorativa, che si svolgeranno a partire dalle 18.30 in piazza Pudente e in piazza del Popolo, le due eleganti aree attorno a Palazzo D’Avalos, che accoglierà a sua volta nei suoi giardini e nel suo chiostro incontri ed eventi collaterali. <br/><br/>Nei colorati stand sarà possibile degustare la ventricina in proposte semplici e dirette, ideali magari per un aperitivo. Oltre ai produttori locali, saranno presenti anche altri espositori con il meglio della produzione enogastronomica di qualità del territorio.<br/><br/> Nell’area ristorazione, invece, sarà possibile scoprire la versatilità della ventricina in cucina, con intriganti e sorprendenti menu a tema ideati per l’occasione: dall’antipasto ai primi ai secondi, sarà l’occasione per assistere alla sublimazione del salume tradizionale vastese, grazie alla creatività di produttori e chef. Completano l’offerta un ricco cartellone di spettacoli, show cooking, laboratori del gusto, visite guidate, convegni, mostre e altro.</p>
