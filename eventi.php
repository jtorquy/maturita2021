                <h5>WineVasto – Degustazione vini<br/><em>7 luglio</em></h5>
                <p>La serata, organizzata dal Fantini Store di Stefania Sabatini e con la collaborazione di Gianluca Pagano, sarà l’opportunità per soddisfare la curiosità di quanti vogliano conoscere più da vicino la realtà vinicola locale, dialogando con i suoi stessi attori.
<br/><br/>
Iasci e Marchesani, Fontefico, San Michele, Iasci e la Farnese by Fantini saranno presenti con i loro stand dove sarà possibile degustare i vini autoctoni, accompagnati da prodotti tipici locali, preparati dai ristoranti presenti nella zona che ospiterà l’evento.
<br/><br/>
Non mancherà l’intrattenimento musicale ad allietare il corso della manifestazione.
<br/><br/>
L’intento non solo è quello promuovere le risorse enogatronomiche locali, ma di trasmettere, attraverso un’occasione di convivialità, i valori e la cultura del territorio inerenti a uno delle sue più grandi realtà rappresentative, il vino.</p>
